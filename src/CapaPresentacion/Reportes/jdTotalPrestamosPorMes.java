    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            VistaReporte.removeAll();
            Container contenedor = this.VistaReporte;
            contenedor.setLayout(new BorderLayout());

            Integer ano = (Integer) cboAno.getSelectedItem();

            Map<String, Object> param = new HashMap<>();
            Locale locale = new Locale("es");
            param.put("ano", ano);
            param.put(JRParameter.REPORT_LOCALE, locale);
            JRViewer vistaRPT = new clsReporte().reporteInterno("rptPrestamosMes.jasper", param);
            contenedor.add(vistaRPT);
            this.VistaReporte.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    public void listar() {
        try {
            clsPrestamo objR = new clsPrestamo();
            ArrayList<Integer> anos = objR.anosPrestamo();
            cboAno.setModel(new DefaultComboBoxModel(anos.toArray()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

