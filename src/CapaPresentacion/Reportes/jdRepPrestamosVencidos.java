    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            VistaReporte.removeAll();
            Container contenedor = this.VistaReporte;
            contenedor.setLayout(new BorderLayout());

            Integer ano = (Integer) cboAno1.getSelectedItem();
            
            int mes = mesesAno();
            
            Map<String, Object> param = new HashMap<>();

            param.put("ano", ano);
            
            
            if (mes != -1) {
                param.put("mes", mes);
            }

            Locale locale = new Locale("es");
            param.put(JRParameter.REPORT_LOCALE, locale);

            JRViewer vistaRPT = new clsReporte().reporteInterno("rptPrestamosVencidos.jasper", param);
            contenedor.add(vistaRPT);
            this.VistaReporte.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void listar() {
        try {
            clsPrestamo objR = new clsPrestamo();
            ArrayList<Integer> anos = objR.anosPrestamo();
            cboAno1.setModel(new DefaultComboBoxModel(anos.toArray()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    public int mesesAno() {

        Map<String, Integer> meses = new HashMap<>();
        meses.put("Enero", 1);
        meses.put("Febrero", 2);
        meses.put("Marzo", 3);
        meses.put("Abril", 4);
        meses.put("Mayo", 5);
        meses.put("Junio", 6);
        meses.put("Julio", 7);
        meses.put("Agosto", 8);
        meses.put("Septiembre", 9);
        meses.put("Octubre", 10);
        meses.put("Noviembre", 11);
        meses.put("Diciembre", 12);
        
        for (String i : meses.keySet()) {
            if (i.equalsIgnoreCase(cboMes.getSelectedItem().toString())) {
                return meses.get(i);
            }
        }
        return -1;
        
    }

