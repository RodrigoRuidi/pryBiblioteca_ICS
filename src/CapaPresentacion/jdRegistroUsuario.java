    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (txtNombre.getText().isEmpty() || txtUsuario.getText().isEmpty() || txtPregunta.getText().isEmpty() || txtContra.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Rellene todo los campos");
            } else {
                objU = new clsUsuario();
                objU.setNombreusu(txtUsuario.getText());
                objU.setNombrecompleto(txtNombre.getText());
                objU.setCargo(cboCargo.getSelectedItem().toString());
                objU.setEstado(cboEstado.getSelectedItem().toString().equalsIgnoreCase("Activo") ? true : false);
                objU.setPreguntasec(txtPregunta.getText());
                objU.setRespuesta(txtRespuesta.getText());
                objU.setContrasena(txtContra.getText());

                Boolean verificarU = objU.verificarNombreU();
                if (verificarU) {
                    JOptionPane.showMessageDialog(this, "El usuario ya existe, intente con otro nombre");
                    txtUsuario.setText("");
                    txtUsuario.requestFocus();
                } else {
                    objU.registarUsuario();
                    JOptionPane.showMessageDialog(this, "El usuario se registr√≥ correctamente");
                    dispose();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char validar = evt.getKeyChar();

        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();

            JOptionPane.showMessageDialog(rootPane, "INGRESAR SOLO LETRAS", "SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtNombreKeyTyped

